package com.accp.domain;

public class Kehutype {
    private Integer typeid;

    private String typename;

    private String typeprice;

    private Integer youxiaoqi;

    private Integer huifei;

    private String huiyuan;

    public Integer getTypeid() {
        return typeid;
    }

    public void setTypeid(Integer typeid) {
        this.typeid = typeid;
    }

    public String getTypename() {
        return typename;
    }

    public void setTypename(String typename) {
        this.typename = typename;
    }

    public String getTypeprice() {
        return typeprice;
    }

    public void setTypeprice(String typeprice) {
        this.typeprice = typeprice;
    }

    public Integer getYouxiaoqi() {
        return youxiaoqi;
    }

    public void setYouxiaoqi(Integer youxiaoqi) {
        this.youxiaoqi = youxiaoqi;
    }

    public Integer getHuifei() {
        return huifei;
    }

    public void setHuifei(Integer huifei) {
        this.huifei = huifei;
    }

    public String getHuiyuan() {
        return huiyuan;
    }

    public void setHuiyuan(String huiyuan) {
        this.huiyuan = huiyuan;
    }
}